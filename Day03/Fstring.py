# Fstrings : it is the morden way to format strings by inserting elements inside them


name = "Nitin"
age = 25
print(f"Hi my name is {name}, i am {age} yr's old")
print(f"Name :- {name}, age :- {age+1}")




#Slicing : it is a way of extracting a part of string using its index positions

x = [1,2,3,4,5,6]
print(x[0:3])  #-- prints [1,2,3]


s = "python"
print(s[0:4])  #-- prints [pyth]


